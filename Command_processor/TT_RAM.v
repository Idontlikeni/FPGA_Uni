`timescale 1ns / 1ps

module TT_RAM #(
    parameter AW = 5
)
(
    // System
    input CLK,
    // STI 1.0
    input S_EX_REQ, // ??????
    input [AW-1:0] S_ADDR, // ?????
    input [2:0] S_CMD, // ???????
    input [7:0] S_D_WR, // ?????? ??? ??????
    output S_EX_ACK, // ?????????????
    output [7:0] S_D_RD, // ?????? ??????
    // Memory Port
    input [AW-1:0] ADDR, // ???? ?????? ???
    output [7:0] DATA // ???? ?????? ???
 );
    
reg [7:0] MEMORY [0:(2**AW)-1];

always @(posedge CLK)
    if (S_EX_REQ & ~S_CMD[2]) MEMORY[S_ADDR] <= S_D_WR;

assign S_D_RD = MEMORY[S_ADDR];
assign S_EX_ACK = 1'b1;

assign DATA = MEMORY[ADDR];

endmodule