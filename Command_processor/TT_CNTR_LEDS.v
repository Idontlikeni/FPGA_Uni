`timescale 1ns / 1ps

module TT_CNTR_LEDS(
    // System
    input CLK,
    input RST,
    // STI 1.0
    input S_EX_REQ, // ???? ???????
    input S_ADDR, // ???? ??????
    input [2:0] S_CMD, // ???? ???????
    input [7:0] S_D_WR, // ???? ?????? ??? ??????
    output S_EX_ACK, // ????? ?????????????
    output [7:0] S_D_RD, // ????? ?????? ??????
    // LED
    output [7:0] LED, // ??????????? ??????? ???????????
    // RAM
    output reg [4:0] ADDR, // ????? ???? ?????? ???
    input [7:0] DATA // ???? ???? ?????? ???
);

reg [7:0] MASK;
wire CE_MASK = S_EX_REQ & (~S_CMD[2]) & (~S_ADDR);

always @(posedge CLK, posedge RST) begin
    if (RST)
        MASK <= 8'b0;
    else if (CE_MASK)
        MASK <= S_D_WR;
end

assign LED = MASK & DATA;

wire CE_ADDR = S_EX_REQ & (~S_CMD[2]) & S_ADDR;

always @(posedge CLK, posedge RST) begin
    if (RST)
        ADDR <= 5'b0; // was 6 bit
    else if (CE_ADDR)
        ADDR <= S_D_WR[4:0]; // was 6 bit
end

assign S_D_RD = (S_ADDR) ? {2'b0, ADDR} : MASK;

assign S_EX_ACK = 1'b1;

endmodule